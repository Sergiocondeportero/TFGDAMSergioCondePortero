private void generarFactura() {
    try {
        // Crear una instancia de la clase GenerarFactura
        GenerarFactura generarFactura = new GenerarFactura();
        
        // Llamamos al m√©todo que genera la factura con los productos de la base de datos
        generarFactura.generarFactura();  // Genera la factura con los productos
        
        // Mostrar un mensaje de √©xito al usuario
        JOptionPane.showMessageDialog(this, "Factura generada con √©xito.");
    } catch (Exception e) {
        // En caso de error, mostramos un mensaje de error
        JOptionPane.showMessageDialog(this, "Error al generar la factura: " + e.getMessage());
    }
}

private void generarReporte() {
    try {
        GenerarReporte.generarDocumento();  // Genera el reporte
        JOptionPane.showMessageDialog(this, "Reporte generado con √©xito.");
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error al generar el reporte: " + e.getMessage());
    }
}
