    private void btnGraficosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGraficosActionPerformed
 // Desmarcar los botones anteriores
    btnHome.setSelected(false);
    btnCategorias.setSelected(false);
    btnClientes.setSelected(false);
    btnProveedores.setSelected(false);
    btnUsuarios.setSelected(false);
    btnProductos.setSelected(false);
    btnAcercaDe.setSelected(false);
    
    // Marcar el bot√≥n de gr√°ficos
    btnGraficos.setSelected(true);
    
    // Crear una instancia de DaoProductos para obtener la lista de productos
    DaoProductos daoProductos = new DaoProductos();
    List<productos> listaProductos = daoProductos.Listar();  // Obtener la lista de productos

    // Verificar que la lista no est√© vac√≠a antes de mostrar el gr√°fico
    if (listaProductos != null && !listaProductos.isEmpty()) {
        // Crear una instancia de la clase Graficos (pasando la lista de productos al constructor)
        Graficos graficoPanel = new Graficos(listaProductos);  // Ahora pasa la lista de productos al constructor

        // Configurar tama√±o y ubicaci√≥n del panel gr√°fico
        graficoPanel.setSize(953, 768);
        graficoPanel.setLocation(0, 0);

        // Limpiar el contenedor y agregar el panel de gr√°ficos
        CONTENEDOR.removeAll();
        CONTENEDOR.add(graficoPanel, BorderLayout.CENTER);  // Aseg√∫rate de agregar correctamente al contenedor

        // Revalidar y repintar para actualizar la interfaz de usuario
        CONTENEDOR.revalidate();
        CONTENEDOR.repaint();
    } else {
        // Mostrar mensaje si no hay productos disponibles
        JOptionPane.showMessageDialog(null, "No hay productos disponibles.");
    }
    }//GEN-LAST:event_btnGraficosActionPerformed

